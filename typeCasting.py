# typecasting :-
# it is casting to convert one datatype to another datatype
# it is of two tyes 
# explicitly casting
# implicitly casting
# it is a casting in which developer or programmer convert datatype into another datatype as per 
# requirements is known as explicit casting conversion
# Example of implicit casting
a="18"
number=7
string_number=int(a)
sum=number + string_number
print(sum)
# it is a casting in which interpreter converters one to another datatype automatically is known as
# implicit casting

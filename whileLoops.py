# While loops in python 
num=int(input("Enter a number :- "))
print("This is Increament While loop")
while (num<20):
    # print number num to 5
    print(num)
    # it will increment add 1
    num=num+1
print("This is Decreament Loop")
num2=-5
while (num2>0):
    # print number num to 20
    print(num2)
    # it will increment subttract 1
    num2=num2-1